fn entry(input: &str) {
    // a = rock      x = rock     1  x = lose
    // b = paper     y = paper    2  y = draw
    // c = scissors  z = scissors 3  z = win
    // Win the round 6 Loss = 0

    let flag: bool = true;

    println!("aoc_2022_02");
    let mut total_score: i32 = 0;
    for line in input.lines() {
        let v = line.split(" ").map(|s| s.to_string()).collect::<Vec<String>>();
        let (them, us) = (v[0].to_lowercase().clone(), v[1].to_lowercase().clone());

        let mut score: i32 = 0;
        let mut new_us: &str = &us;

        if flag {
            new_us = match us.as_str() {
                "x" => match them.as_str() {
                    "a" => "z",
                    "b" => "x",
                    "c" => "y",
                    _ => ""
                },
                "y" => match them.as_str() {
                    "a" => "x",
                    "b" => "y",
                    "c" => "z",
                    _ => ""
                },
                "z" => match them.as_str() {
                    "a" => "y",
                    "b" => "z",
                    "c" => "x",
                    _ => ""
                },
                _ => ""
            };
            println!("them = {}, us = {}", them, us);
        } else {
            new_us = &us;
        }

        match them.as_str() {
             "a" => {
                match new_us {
                    "x" => score = 1 + 3,
                    "y" => score = 2 + 6,
                    "z" => score = 3 + 0,
                    _ => {}
                }
            },
            "b" => {
                match new_us {
                    "x" => score = 1 + 0,
                    "y" => score = 2 + 3,
                    "z" => score = 3 + 6,
                    _ => {}
                }
            },
            "c" => {
                match new_us {
                    "x" => score = 1 + 6,
                    "y" => score = 2 + 0,
                    "z" => score = 3 + 3,
                    _ => {}
                }
            },
            _ => {}
        };

        total_score += score;
    }
    println!("total_score = {}", total_score);
}

